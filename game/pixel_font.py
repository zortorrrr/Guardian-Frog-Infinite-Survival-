_CHAR_W = 5
_CHAR_H = 7
_SPACING = 1   # gap between chars


class PixelFont:
    """Scalable pixel bitmap font. scale=2 → each art-pixel is 2×2 screen-px."""

    def __init__(self, scale: int = 2) -> None:
        self.scale = scale
        self.char_w = (_CHAR_W + _SPACING) * scale
        self.char_h = _CHAR_H * scale

    def text_width(self, text: str) -> int:
        return len(text) * self.char_w

    def draw(
        self,
        surface: pygame.Surface,
        text: str,
        x: int,
        y: int,
        color: tuple = (255, 255, 255),
        shadow: tuple | None = (20, 20, 30),
        outline: tuple | None = None,
    ) -> None:
        """Draw text left-aligned at (x, y)."""
        S = self.scale

        for ci, ch in enumerate(text.upper()):
            glyph = _GLYPHS.get(ch, _GLYPHS.get(' '))
            if glyph is None:
                continue
            cx = x + ci * self.char_w

            for ry, row in enumerate(glyph):
                for rx, bit in enumerate(row):
                    if bit == '1':
                        sx = cx + rx * S
                        sy = y  + ry * S
                        # drop shadow
                        if shadow:
                            pygame.draw.rect(surface, shadow,
                                             (sx+1, sy+1, S, S))
                        # outline (draw around)
                        if outline:
                            for dx, dy in [(-1,0),(1,0),(0,-1),(0,1)]:
                                pygame.draw.rect(surface, outline,
                                                 (sx+dx, sy+dy, S, S))
                        pygame.draw.rect(surface, color, (sx, sy, S, S))

    def draw_centered(
        self,
        surface: pygame.Surface,
        text: str,
        cx: int,
        y: int,
        **kwargs,
    ) -> None:
        x = cx - self.text_width(text) // 2
        self.draw(surface, text, x, y, **kwargs)

    def draw_right(
        self,
        surface: pygame.Surface,
        text: str,
        rx: int,
        y: int,
        **kwargs,
    ) -> None:
        x = rx - self.text_width(text)
        self.draw(surface, text, x, y, **kwargs)